"""
    Library module.
"""


def add_two_numbers(x: int, y: int) -> int:
    """
    Add two numbers.

    :param x: first whole number
    :param y: second whole number
    """
    return x + y


def multiply_two_numbers(x: int, y: int) -> int:
    """
    Multiply two numbers.

    :param x: first whole number
    :param y: second whole number
    """
    return x * y
